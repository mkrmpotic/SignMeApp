package eu.signme.app.model;

public class Comrade {

	private int beer;
	private String name;

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getBeer() {
		return this.beer;
	}

	public void setBeer(int beer) {
		this.beer = beer;
	}

}
